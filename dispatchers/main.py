def replay_replay_batch(
    records: List[Dict[str, Any]],
    batch_size: int = 50,
) -> List[List[Dict[str, Any]]]:
    """Slice *records* into chunks of *batch_size* for bulk replay."""
    return [records[i : i + batch_size]
            for i in range(0, len(records), batch_size)]
